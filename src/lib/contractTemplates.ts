/** Подставляет переменные {{ключ}} в текст шаблона. Отсутствующие значения — тире. */
export function fillTemplate(template: string, vars: ContractVars): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const v = (vars as any)[key];
    return v !== undefined && v !== null && v !== '' ? String(v) : '—';
  });
}

/**
 * Определяет, является ли строка заголовком раздела документа (например
 * "1. ПРЕДМЕТ ДОГОВОРА" или "ОТВЕТСТВЕННОСТЬ И РАЗРЕШЕНИЕ СПОРОВ"), чтобы
 * визуально выделить её при выводе — иначе весь договор выглядит одним
 * сплошным полотном текста без структуры.
 */
export function isContractHeaderLine(line: string): boolean {
  const trimmed = line.trim();
  if (!trimmed || trimmed.length > 80) return false;
  const lettersOnly = trimmed.replace(/[^A-Za-zА-Яа-яЁёЎўҚқҒғҲҳІіӘәӨөҮүҢң]/g, '');
  if (!lettersOnly) return false;
  return lettersOnly === lettersOnly.toUpperCase();
}
